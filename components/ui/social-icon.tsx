function inferFromUrl(href?: string): SocialName | undefined {
  if (!href) return;
  const norm = href.match(/^https?:\/\//i) ? href : `https://${href}`;
  try {
    const host = new URL(norm.toLowerCase()).hostname.replace(/^www\./, "");
    for (const [domain, key] of DOMAIN_RULES) {
      if (host === domain || host.endsWith(`.${domain}`)) return key;
    }
    return "website";
  } catch {
    return "website";
  }
}

